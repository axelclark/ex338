import Config

# config/runtime.exs is executed for all environments, including
# during releases. It is executed after compilation and before the
# system starts, so it is typically used to load production configuration
# and secrets from environment variables or elsewhere. Do not define
# any compile-time configuration in here, as it won't be applied.
# The block below contains prod specific runtime configuration.

if config_env() == :prod do
  # For production, we configure the host to read the PORT
  # from the system environment. Therefore, you will need
  # to set PORT=80 before running your server.
  #
  # You should also configure the url host to something
  # meaningful, we use this information when generating URLs.
  #
  # Finally, we also include the path to a manifest
  # containing the digested version of static files. This
  # manifest is generated by the mix phoenix.digest task
  # which you typically run after static files are built.
  database_url =
    System.get_env("DATABASE_URL") ||
      raise """
      environment variable DATABASE_URL is missing.
      For example: ecto://USER:PASS@HOST/DATABASE
      """

  # Configure your database
  config :ex338, Ex338.Repo,
    adapter: Ecto.Adapters.Postgres,
    url: database_url,
    pool_size: String.to_integer(System.get_env("POOL_SIZE") || "10"),
    ssl: true,
    ssl_opts: [verify: :verify_none]

  # The secret key base is used to sign/encrypt cookies and other secrets.
  # A default value is used in config/dev.exs and config/test.exs but you
  # want to use a different value for prod and you most likely don't want
  # to check this value into version control, so we use an environment
  # variable instead.
  secret_key_base =
    System.get_env("SECRET_KEY_BASE") ||
      raise """
      environment variable SECRET_KEY_BASE is missing.
      You can generate one by calling: mix phx.gen.secret
      """

  host = System.get_env("HOST") || "example.com"

  # For production, we configure the host to read the PORT
  # from the system environment. Therefore, you will need
  # to set PORT=80 before running your server.
  #
  port = String.to_integer(System.get_env("PORT") || "4000")

  config :ex338, Ex338Web.Endpoint,
    http: [port: port, compress: true],
    url: [scheme: "https", host: host, port: 443],
    force_ssl: [rewrite_on: [:x_forwarded_proto]],
    secret_key_base: secret_key_base

  config :ex338, :pow_redis, uri: System.get_env("REDIS_URL")

  # ## SSL Support
  #
  # To get SSL working, you will need to add the `https` key
  # to your endpoint configuration:
  #
  #     config :phx_template_1_7_11, PhxTemplate1711Web.Endpoint,
  #       https: [
  #         ...,
  #         port: 443,
  #         cipher_suite: :strong,
  #         keyfile: System.get_env("SOME_APP_SSL_KEY_PATH"),
  #         certfile: System.get_env("SOME_APP_SSL_CERT_PATH")
  #       ]
  #
  # The `cipher_suite` is set to `:strong` to support only the
  # latest and more secure SSL ciphers. This means old browsers
  # and clients may not be supported. You can set it to
  # `:compatible` for wider support.
  #
  # `:keyfile` and `:certfile` expect an absolute path to the key
  # and cert in disk or a relative path inside priv, for example
  # "priv/ssl/server.key". For all supported SSL configuration
  # options, see https://hexdocs.pm/plug/Plug.SSL.html#configure/1
  #
  # We also recommend setting `force_ssl` in your config/prod.exs,
  # ensuring no data is ever sent via http, always redirecting to https:
  #
  #     config :phx_template_1_7_11, PhxTemplate1711Web.Endpoint,
  #       force_ssl: [hsts: true]
  #
  # Check `Plug.SSL` for all available options in `force_ssl`.

  # ## Configuring the mailer
  #
  # In production you need to configure the mailer to use a different adapter.
  # Also, you may need to configure the Swoosh API client of your choice if you
  # are not using SMTP. Here is an example of the configuration:
  #
  #     config :phx_template_1_7_11, PhxTemplate1711.Mailer,
  #       adapter: Swoosh.Adapters.Mailgun,
  #       api_key: System.get_env("MAILGUN_API_KEY"),
  #       domain: System.get_env("MAILGUN_DOMAIN")
  #
  # For this example you need include a HTTP client required by Swoosh API client.
  # Swoosh supports Hackney and Finch out of the box:
  #
  #     config :swoosh, :api_client, Swoosh.ApiClient.Hackney
  #
  # See https://hexdocs.pm/swoosh/Swoosh.html#module-installation for details.
  config :ex338, Ex338Web.Mailer,
    adapter: Swoosh.Adapters.AmazonSES,
    region: "us-east-1",
    access_key: System.get_env("AWS_SES_ACCESS_KEY"),
    secret: System.get_env("AWS_SES_SECRET")

  config :ex338, Ex338Web.PowMailer,
    adapter: Swoosh.Adapters.AmazonSES,
    region: "us-east-1",
    access_key: System.get_env("AWS_SES_ACCESS_KEY"),
    secret: System.get_env("AWS_SES_SECRET")
end
